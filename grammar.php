<h3 class="content_title" id="honorifics">KHAJIIT HONORIFICS</h3>

<p>In Khajiiti culture, individuals will often take an optional honorific on their name. These honorifics may be chosen by the individual or given to him or her by clan or family. Some Khajiit consider it foolish or poor form to take more than one honorific.
<br>
Honorifics be either prefixes or suffixes. When adding an honorific prefix, the honorific is separated from the name with an apostrophe and the name is capitalized. For example: Zakhar with the honorific prefix "ra" would be "Ra'Zakhar". When adding an honorific suffix, the honorific is separated from the name with a dash and the honorific is not capitalized. For example: Garesh with the honorific suffix "ri" would be "Garesh-ri".
<br>
Listed below are some of the most common khajiiti name honorifics. Honorifics marked with an asterisk * are non-canon and have been added by us or others in the community.
<ul>
<li>dar: Male specific. This honorific means "thief," but more like the Nordic "Clever Hudvar" or the Breton "Arnand the Fox." A Khajiit with "dar" in his name is clever, and maybe clever with his hands, but not always a thief by your odd Imperial property customs.</li>
<li>daro: Female specific. Female version of dar.</li>
<li>do: This honorific means "warrior" and is almost exclusively male, but is very occasionally accorded to outstanding females.</li>
<li>dra: Female specific. Grandmother or wise elder woman.</li>
<li>dro: Male specific. Grandfather or patriarch.</li>
<li>ja/ji/j/sa: Male specific. Bachelor.</li>
<li>jo: Male specific. Wizard, scholar, or healer.</li>
<li>ko: Female specific. Wizard, scholar, or healer.</li>
<li>la: Female specific. High-caste unmarried female or maiden.</li>
<li>ma/m: Gender neutral. Young, child, virgin.</li>
<li>*qa: A rare title of unknown meaning.</li>
<li>ra: Male specific. Boss, leader, male of high standing.</li>
<li>ri: Male specific. General, great leader, male of highest standing.</li>
<li>*ro: Female specific. Noblewoman, female of high standing.</li>
<li>*s: Gender neutral. Adult or elder.</li>
</ul>
</p>

<h3 class="content_title" id="structure">SENTENCE STRUCTURE</h3>
<p>
	Ta'agra, like English, primarily uses a Subject-Verb-Object (SVO) word order. Ta'agra has no markers for the subject or objects of a sentence, so word order is important to conveying meaning. (e.g. "Man bites dog" is very different from "dog bites man".)<br><br>
	Comments on word order: Existing lore samples of Ta'agra are inconsistent in their use of word order. Some are clearly Subject-Verb-Object and some are clearly Subject-Object-Verb. After extensive discussion on this topic, we have decided to stick with a Subject-Verb-Object order for Ta'agra. This will increase simplicity and ease of use, because this is the same word order used by native English speakers. We attribute the discrepancy to cultural and linguistic variations between Anaquina and Pa'alatiin with the north historically using SOV and the south using SVO. Both structures work, so long as the Subject comes at the beginning of the sentence. We are saying the official structure, however, is SVO.
</p>

<h3 class="content_subtitle">Intonation</h3>
<p>
Declarative sentences in Ta'agra have no special intonation. Interrogative sentences (questions) end with an upwards inflection at the end, just like English.
</p>

<hr>

<h3 class="content_title" id="pronunciation"><small>PRONUNCIATION</small></h3>
<p>
	Pronunciation in Ta'agra is fairly straightforward. Some consonant pairs can be challenging, but consonants and vowels are consistent in the sounds they make, with the exception of J (though even many native speakers don't enunciate the distinction between the initial J and the medial/terminal J).<br><br>
	Though not technically proper, most Ta'agra speakers will not double-enunciate consonants when one word ends with a consonant and the next word begins with that same consonant. For example "someone pulled his tail" translates to "egahz zanka jan zeva". It is common for "egahz" and "zanka" to simply be blanded together as "egahzanka" rather than enunciating the Z twice.
</p>

<hr>

<h3 class="content_title">NOUNS</h3>

<h3 class="content_subtitle">Case</h3>

<ul>
	<li>Nominative (Subject): No change.</li>
	<li>Accusative (Object): No change.</li>
	<li>Dative (Indirect Object): No change.</li>
	<li>Genetive (Possessive): No change, indicated by word order. When two nouns are paired together in a sentence, the first noun is the possessor and the second noun is the possessed. e.g. "Ra'Zakhar's bread" = "Ra'Zakhar barij". Alternatively, if more clarity is needed, possession can be established by using the word "di" (of): "Barij di Ra'Zakhar" = "Bread of Ra'Zakhar".</li>
	<li>Lative (Movement to something.): +ali. "Ra'Zakhar walks to the market" = "Ra'Zakhar iit shothali".</li>
<ul>
	<li>Note: English indicates infinitives with "to". This use of "to" does not translate to +ali in Ta'agra, it is simply omitted. For example, "I like to walk to the market" translates to "Ahziss kefa iit shothali" (I like walk market-to).</li>
</ul>
	<li>Ablative (Movement from something. Also used to indicate origin.): +iitay. "Ra'Zakhar is from Torval" = "Ra'Zakhar vaba Torvaliitay".</li>
</ul>

<h3 class="content_subtitle">Plurals</h3>
<p>
	Plural: +a (ends w/ cons), +'a (end w/ a, ay, ai), drop ending vowel & +a (ends with e, i, o u).
	<ul>
		<li>Naraj = animal. Naraja = animals.</li>
		<li>Kafa = horse. Kafa'a = horses. </li>
		<li>Raszai = drug. Razsai'a = drugs. </li>
		<li>Srato = hat. Srata = hats. </li>
		<li>Alku = bottle. Alka = bottles.</li>
	</ul>
</p>

<h3 class="content_subtitle">Agentive</h3>
  <p>Verb/noun to agentive noun: +iit.  If the word ends in a vowel, drop the vowel before adding the suffix.</p>
<ul>
  <li>Budi = shirt.  Budiit = tailor (one who shirts, shirter)</li>
  <li>Khaj = desert/sand.  Khajiit = (one who deserts, deserter)</li>
  <li>Dela = learn.  Deliit = student (one who learns, learner) </li>
	<li>Laa = law.  Aliit = (one who laws, lawyer).  <I>Special irregular: Single syllable words consisting of a consonant and a vowel would become too short.  Instead of dropping the trailing vowel entirely, the vowel is moved to the front of the word.</I></li>
</ul>

<h3 class="content_subtitle">Nominalization<br><small>(Converting verbs/adjectives/adverbs to nouns)</small></h3>
<ul>
	<li>Word +na.  (If root word ends with Noun + H, drop the H before adding +na.  e.g. valah = far.  valana = distance)</li>
		<ul>
			<li>Takarr = [to] attack(v).  Takarrna = [an] attack(n).</li>
		  <li>Dar = steal(v).  Darna = theft.</li>
		</ul>
  <li>If nominalizing an adjective formed by adding +i/+'i to a noun root word, keep the adjective suffix and add +na to the end of it.</li>
		<ul>
			<li>Zal = health(n).  Zali = healthy(adj).  Zalina = healthiness(n).</li>
		</ul>
  <li>If nominalizing an adjective that is a root word, just add +na.</li>
		<ul>
		  <li>Itrola = ugly.  Itrolana = ugliness.</li>
		</ul>
  <li>If pluralizaing a nominalized word, follow the same pluralization rules for words ending with the letter A (+'a).</li>
		<ul>
		  <li>Sa'alai = pray(v).  Sa'alaina = prayer(n).  Sa'alaina'a = prayers(n).</li>
	</ul>
</ul>

