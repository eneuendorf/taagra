<P>Ta'agra does not use the Perfect (I have/had played) or Perfect Continuous (I have/had been playing) verb tenses.</P><P>Simple common verbs are often truncated for brevity.  For example, "vara" may be said simply as "ra" and vaba may be said simply as "ba".  The Khajiit often look for language shortcuts.</P>

<h3 class="content_subtitle">Negation</h3>
<p>Ta'agra uses a negation suffix on verbs instead of using an independent word to establish negation (e.g. "not" in English).</p>
<ul>
	<li>Present tense: +oh.</li>
	<li>Past tense: +ohka.</li>
	<li>Future tense: +ohse.</li>
</ul>
	<p>If the word ends in a vowel, drop the vowel before adding the suffix.</p>
	<ul>
		<li>"I worry" = "Ahziss muk"</li>
		<li>"I don't worry" = "Ahziss mukoh".</li>
		<li>"I mentioned that" = "Ahziss bekka jaji".</li>
		<li>"I didn't mention that" = "Ahziss bekohka jaji".</li>
		<li>"I will fight" = "Ahziss rakse".</li>
		<li>"I will not fight" = "Ahziss rakohse".</li>
	</ul>

<h3 class="content_subtitle">Voice</h3>
  <p>Ta'agra can use both passive and active voice.  The active voice, which is short and direct, is usually preferred.</p>

	<p>Active:</p>
	<ul>
  <li>"The hunter killed the animal."</li>
  <li>"Hirsiniit var darka naraj."</li>
  </ul>
  <p>Passive:</p>
	<ul>
  <li>"The animal was killed by the hunter."</li>
  <li>"Naraj vaber var darka iho hirsiniit."</li>
	</ul>

<h3 class="content_subtitle">Mood</h3>
  <p>Ta'agra, like English, establishes Grammatical moods (condition, command, hope, etc.) with helper words rather than through verb inflection.</p>

<h3 class="content_subtitle">Converting Nouns to Verbs</h3>
  <p>Noun to Verb: +ith  If the word ends in a vowel, drop the vowel before adding the suffix.</p>
	<ul>
  	<li>da'khe = water.  da'khith = to water/watering</li>
  	<li>zrasha = a circle.  zrashith = to circle/circling</li>
</ul>

