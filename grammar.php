<h3 class="content_title" id="adjectives">ADJECTIVES</h3>
	<p>Adjectives/nouns modifying nouns: Combine the two (or more) words with the adjective(s) before the noun being modified.  If the last letter of the adjective and the first letter of the noun are both vowels, separate with a glottal stop (').
  <BR>- Zwinthodurrarr = yellow pen (zwintho + durrarr)
  <BR>- Ajo'iiliten = wonderful girl (ajo + ' + iiliten)
  <BR>- Fithsutapal = hot, long day (fith + suta + pal)
  <br><br>While traditional Ta'agra will combine adjectives with the nouns they modify to form compound words, modern speakers influenced by other Tamrielic languages will often separate adjectives from the nouns they modify with a space.</P>
<P><h3><b>Noun Adjunct: Using Nouns as Adjectives</h3></b>
  <BR>Nouns can be used as adjectives without modification
  <BR>- dog = rian.  food = taj.  dog food = riantaj.
</P>
<P><h3><b>Converting Nouns to Adjectives</h3></b>
  <BR>Noun to Adjective: +i (ends w/ cons), drop ending vowel sound & +'i (ends w/ vowel).  <BR>- dog = rian.  doglike/canine(adj) = riani
</P>
<P>It is not necessary to convert a noun to its adjective form when using that noun as an adjective.  These two forms can be used to create subtle differences in words.
  <BR>- dogfood = riantaj.  doglike food = rianitaj
</P>
<hr>

<h3 class="content_title" id="adverbs">ADVERBS</h3>
  <BR>Ta'agra does not have a consistent rule for forming adverbs from other words.  Adverbs will occasionally be formed with the +i suffix like adjectives, but not always.</P>
<hr>

<h3 class="content_title" id="pronouns">PRONOUNS</h3>
  <BR>It is a common misconception that the Khajiit do not use pronouns.  Ta'agra has pronouns, though the use, origin, and meaning of these words reflects the Khajiiti culture.  Pronouns are sometimes omitted, especially when using certain irregular verbs where the conjugation implies the pronoun that was excluded.  For a list of Ta'agra pronouns please refer to the <A HREF="pronouns.php">Pronouns</A> page or the <A HREF="dictionary.php">Dictionary</A>.
  <br><br><i>TTP Staff Opinion: The most common misconception is that Ta'agra has no word for "I".  This is not correct.  The Ta'agra word for "I" is "ahziss".  The reason for this misconception is likely due to the structure of the word.  "Ahziss" is comprised of "ahz" (person) and "iss" (people).  A rough translation could be "one of the people" or "a person of the people".  This may be why most khajiit prefer to speak of themselves in the third person when not speaking Ta'agra, as it more closely reflects the meaning of the word in Ta'agra.</i></P>
<hr>

<h3 class="content_title" id="prepositions">PREPOSITIONS</h3>
  <BR>Ta'agra's usage of prepositions is the same as in English.  Prepositions are used to relate a noun or pronoun to another word or element in a clause.
</P>
<hr>

<h3 class="content_title" id="conjunctions">CONJUNCTIONS</h3>
  <BR>Ta'agra uses most of the standard English conjunctions (and, but, or, because, etc.).  Like English, conjunctions are used to join sentences, clauses, and words.
</P>
<hr>

<h3 class="content_title" id="articles">ARTICLES</h3>
  <BR>Ta'agra does not use direct or indirect articles (a, an, the).
</P>
<hr>

<h3 class="content_title" id="contractions">CONTRACTIONS</h3>
<p>The khajiit are always looking for ways to simplify their language, this has lead to numerous common contractions.<br>Some examples include:</p>
<ul>
	<li>daba (dat vaba): It is (it's)</li>
	<li>jaba (jan vaba): He is (he's)</li>
	<li>roba (roj vaba): She is (she's)</li>
</ul>

<hr>

<h3 class="content_title" id="suffixes">SUFFIXES</h3>
<p>Ta'agra has various suffixes which can change the meaning of words.</p>
<ul>
	<li>+ozay (without, +less): fus (regret)</li>
	<li>fusozay (regretless/without-regret)</li>
</ul>
